package com.sana.circleup;

public class Member {
    private String id;
    private String name;
    private String role;

    // Constructor
    public Member(String id, String name) {
        this.id = id;
        this.name = name;
        this.role = role;
    }

    // Getters
    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getRole() {
        return role;
    }
}
